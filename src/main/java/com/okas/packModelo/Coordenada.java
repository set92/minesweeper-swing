package com.okas.packModelo;

public class Coordenada {
    int posAlto;
    int posAncho;
    
    Coordenada(int pAlto, int pAncho){
    	this.posAlto = pAlto;
    	this.posAncho = pAncho;
    }

    public int getPosAlto() {
        return posAlto;
    }

    public int getPosAncho() {
        return posAncho;
    }

}
